
class Calculator:
    def __init__(self):
        pass

    def add(self, first_num, second_num):
        return first_num + second_num


    def subt(self, first_num, second_num):
        return first_num - second_num


    def multi(self, first_num, second_num):
        return first_num * second_num


    def div(self, first_num, second_num):
        return first_num / second_num
